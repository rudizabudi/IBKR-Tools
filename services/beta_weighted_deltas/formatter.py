from math import copysign
from statistics import mean


type Position = 'Position'

def generate_selection_list(positions):
    if positions:
        positions_str_sorted = ['Overview', 'Portfolio']
        positions_str_sorted.extend(sorted(list(set(x.get_symbol() for x in positions))))
        return positions_str_sorted


class TableContentGenerator:
    def __init__(self, core):
        self.core = core
        self.total_line = None

        self.table_contents = core.table_contents

    def generate_position_cells(self, header: dict[str: str | float], positions: list[Position]):

        def add_empty_row():
            self.table_contents[header['name']].append([])
            for _ in range(9):
                self.table_contents[header['name']][-1].append([])

        if header['name'] in ['Overview', 'Portfolio']:
            return

        self.table_contents[header['name']] =[]
        add_empty_row()

        self.table_contents[header['name']][0][0] = header['name']

        beta = header['beta']
        self.table_contents[header['name']][0][1] = round(beta, 2)

        try:
            underlying = list(filter(lambda x: x.get_secType() in ['STK'], positions.copy()))[0]
        except IndexError:
            underlying = None

        avg_ivol, sum_delta, sum_bwd, sum_theta, l_gamma, s_gamma, not_pos = [], [], [], [], [], [], []
        if underlying:
            add_empty_row()
            self.table_contents[header['name']][-1][1] = underlying.generate_name()
            self.table_contents[header['name']][-1][2] = underlying.get_pos_size()

            delta = self.table_contents[header['name']][-1][2]
            sum_delta.append(delta)
            self.table_contents[header['name']][-1][4] = f'{delta:,.2f}'
            sum_bwd.append(delta * beta)
            self.table_contents[header['name']][-1][5] = f'{delta * beta:,.2f}'
            not_pos.append(self.table_contents[header['name']][-1][2] * underlying.get_price())
            self.table_contents[header['name']][-1][8] = f'{self.table_contents[header['name']][-1][2] * underlying.get_price():,.0f}'

            underlying_price = underlying.get_price()
        else:
            underlying_price = self.core.underlying_prices[header['name']]

        for position in positions:
            if position != underlying:
                add_empty_row()
                self.table_contents[header['name']][-1][1] = position.generate_name()

                self.table_contents[header['name']][-1][2] = f'{position.get_pos_size():.0f}'

                try:
                    avg_ivol.append(position.get_greeks()['iVol'])
                    self.table_contents[header['name']][-1][3] = f'{position.get_greeks()['iVol'] * 100:.0f}%'
                    #self.table_contents[header['name']][-1][3] = f'{position.get_greeks().get('iVol', 0) * 100:.0f}%'

                except KeyError:
                    avg_ivol.append(0)
                    self.table_contents[header['name']][-1][3] = f'{0 * 100:.0f}%'

                    print(f'KeyError:Position {position.generate_name()} has no IVOL')

                delta = position.get_greeks()['delta'] * copysign(position.get_pos_size(), position.get_pos_size())
                sum_delta.append( delta * 100)
                self.table_contents[header['name']][-1][4] = f'{ delta * 100:.2f}'

                sum_bwd.append(delta * 100 * beta)
                self.table_contents[header['name']][-1][5] = f'{delta * 100 * beta:.2f}'

                sum_theta.append(position.get_greeks()['theta'])
                self.table_contents[header['name']][-1][6] = f'{position.get_greeks()['theta'] * 100:.2f}'

                if (' Call ' in position.generate_name() and position.get_pos_size() > 0) or (' Put ' in position.generate_name() and position.get_pos_size() < 0):
                    l_gamma.append(position.get_greeks()['gamma'])
                elif (' Call ' in position.generate_name() and position.get_pos_size() < 0) or (' Put ' in position.generate_name() and position.get_pos_size() > 0):
                    s_gamma.append(position.get_greeks()['gamma'])
                self.table_contents[header['name']][-1][7] = f'{position.get_greeks()['gamma'] * 100:.2f}'

                not_pos.append(delta * underlying_price * 100)
                self.table_contents[header['name']][-1][8] = f'{delta * underlying_price * 100:,.0f}'

        self.table_contents[header['name']][0][3] = f'{mean(avg_ivol) * 100:.0f}%'
        self.table_contents[header['name']][0][4] = f'{sum(sum_delta):.2f}'
        self.table_contents[header['name']][0][5] = f'{sum(sum_bwd):.2f}'
        self.table_contents[header['name']][0][6] = f'{sum(sum_theta) * 100:.2f}'
        self.table_contents[header['name']][0][7] = f'{sum(l_gamma) * 100:.2f} | {sum(s_gamma) * 100:.2f}'
        self.table_contents[header['name']][0][8] = f'{sum(not_pos):,.0f}'

        # for x in self.table_contents[header['name']]:
        #     print(x)
        # print(' - - - ')

    def calculate_total_line(self):

        self.total_line = [[] for _ in range(9)]

        beta_avg, ivol_avg, delta_sum, bwd_sum, theta_sum, gamma_sum_positive, gamma_sum_negative , not_pos_sum = [], [], [], [], [], [], [], []

        for header in self.table_contents:
            beta_avg.append(float(self.table_contents[header][0][1]))
            ivol_avg.append(int(self.table_contents[header][0][3][:-1]))
            delta_sum.append(float(self.table_contents[header][0][4]))
            bwd_sum.append(float(self.table_contents[header][0][5]))
            theta_sum.append(float(self.table_contents[header][0][6]))
            gamma_sum_positive.append(float(self.table_contents[header][0][7].split(' | ')[0]))
            gamma_sum_negative.append(float(self.table_contents[header][0][7].split(' | ')[1]))
            not_pos_sum.append(float(self.table_contents[header][0][8].replace(',', '')))

        self.total_line[0] = f'TOTAL'
        self.total_line[1] = f'{mean(beta_avg):.2f}'
        self.total_line[3] = f'{mean(ivol_avg):.0f}%'
        self.total_line[4] = f'{sum(delta_sum):.2f}'
        self.total_line[5] = f'{sum(bwd_sum):.2f}'
        self.total_line[6] = f'{sum(theta_sum):.2f}'
        self.total_line[7] = f'{sum(gamma_sum_positive):.2f} | {sum(gamma_sum_negative):.2f}'
        self.total_line[8] = f'{sum(not_pos_sum):,.0f}'

    def generate_portfolio_cells(self):
        self.table_contents['Portfolio'] = []
        for key in self.table_contents.keys():
            if key not in ['Overview', 'Portfolio']:
                for line in self.table_contents[key]:
                    self.table_contents['Portfolio'].append(line)

                self.table_contents['Portfolio'].append([[] for _ in range(9)])

        self.table_contents['Portfolio'].append(self.total_line)

    def generate_overview_cells(self):
        self.table_contents['Overview'] = []
        for key in self.table_contents.keys():
            if key not in ['Overview', 'Portfolio']:
                for line in self.table_contents[key]:
                    if line[0]:
                        self.table_contents['Overview'].append(line)

                self.table_contents['Overview'].append([[] for _ in range(9)])

        self.table_contents['Overview'].append(self.total_line)

    def get_table_contents(self) -> dict[list[str | float]]:
        return self.table_contents

